export function getEnhancedExtractionPrompt(): string {
  return `أنت مساعد متخصص في استخراج البيانات من صور الإيصالات والفواتير العراقية.
مهمتك هي استخراج البيانات التالية بدقة وإرجاعها في صيغة JSON محددة:

1. اسم الشركة (companyName): اسم الشركة أو المؤسسة الموجودة في الإيصال.
2. الكود (code): رقم الإيصال أو الفاتورة أو رقم التتبع (هذا مهم جداً).
3. اسم المرسل (senderName): اسم العميل أو المرسل أو الزبون.
4. رقم الهاتف (phoneNumber): يجب أن يكون 11 رقم ويبدأ بـ 07.
5. المحافظة (province): إحدى محافظات العراق مثل بغداد، البصرة، أربيل، إلخ.
6. السعر (price): المبلغ بالدينار العراقي.

اقرأ جميع النصوص في الصورة بعناية، بما في ذلك النصوص المكتوبة بخط اليد والنصوص المطبوعة.

ثم أرجع البيانات المستخرجة في صيغة JSON فقط كما في المثال التالي:

\`\`\`json
{
  "companyName": "شركة النقل السريع",
  "code": "123456",
  "senderName": "محمد أحمد",
  "phoneNumber": "07701234567",
  "province": "بغداد",
  "price": "25000"
}
\`\`\`

قواعد مهمة:
- رقم الهاتف العراقي يجب أن يكون 11 رقم بالضبط ويبدأ بـ 07.
- السعر يجب أن يكون رقماً فقط بدون علامات أو وحدات.
- إذا لم تتمكن من تحديد أي حقل، اتركه فارغاً.
- تأكد من استخراج البيانات بشكل صحيح حتى لو كانت مكتوبة بخط اليد.
- أرجع JSON فقط بدون أي نص إضافي.

هذه مهمة حرجة، ركز بشكل خاص على دقة استخراج البيانات الرئيسية: الكود، اسم المرسل، رقم الهاتف، والمحافظة.`;
}

export function getBasicExtractionPrompt(): string {
  return `قم بتحليل هذه الصورة واستخراج البيانات التالية وإرجاعها في صيغة JSON:

1. اسم الشركة (companyName)
2. الكود أو رقم الوصل (code)
3. اسم المرسل أو الزبون (senderName)
4. رقم الهاتف - يجب أن يكون 11 رقم ويبدأ بـ 07 (phoneNumber)
5. المحافظة العراقية (province)
6. السعر (price)

أرجع البيانات في صيغة JSON فقط:

\`\`\`json
{
  "companyName": "",
  "code": "",
  "senderName": "",
  "phoneNumber": "",
  "province": "",
  "price": ""
}
\`\`\``;
}

// مطالبة مُحسّنة للتركيز على استخراج النص بأعلى دقة ممكنة
export function getTextOnlyExtractionPrompt(): string {
  return `مهمتك هي استخراج كل النص المرئي من هذه الصورة بدقة عالية، ثم البحث عن:

1. اسم الشركة
2. الكود أو رقم الوصل
3. اسم المرسل أو الزبون
4. رقم الهاتف (يجب أن يكون 11 رقم ويبدأ بـ 07)
5. المحافظة العراقية
6. السعر

ضع البيانات التي تستطيع استخراجها في صيغة JSON فقط:

\`\`\`json
{
  "companyName": "",
  "code": "",
  "senderName": "",
  "phoneNumber": "",
  "province": "",
  "price": ""
}
\`\`\``;
}

