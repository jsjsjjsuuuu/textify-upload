/**
 * مطالبة استخراج محسنة للوصولات العراقية
 */
export const getEnhancedExtractionPrompt = (): string => {
  return `
انت مساعد ذكي متخصص في استخراج بيانات الوصولات العراقية.
قم بتحليل هذه الصورة التي تحتوي على وصل شحن وأزلت البيانات التالية:

1. الكود: رقم الوصل/الشحنة (عادة يبدأ برمز أو رقم)
2. اسم المرسل: الاسم الكامل للمرسل
3. رقم الهاتف: رقم الهاتف المكون من 11 رقم (عادة ما يبدأ بـ 079، 077 أو 078)
4. المحافظة: اسم المحافظة العراقية (مثل بغداد، البصرة، أربيل، إلخ)
5. السعر: قيمة الشحنة بالدينار العراقي
6. اسم الشركة: اسم شركة الشحن أو التوصيل (إذا كان موجودًا)

المطلوب تنسيق الإخراج كـ JSON بالشكل التالي:
{
  "code": "رقم الكود هنا",
  "senderName": "اسم المرسل هنا",
  "phoneNumber": "رقم الهاتف هنا",
  "province": "اسم المحافظة هنا",
  "price": "السعر هنا",
  "companyName": "اسم الشركة هنا",
  "confidence": "مستوى الثقة بالنتائج (من 0 إلى 100)"
}

ملاحظات هامة:
- إذا كانت أي من المعلومات غير موجودة، استخدم سلسلة فارغة.
- لا تقم بتخمين البيانات، اكتف بما هو موجود في الصورة.
- تأكد من عرض رقم الهاتف بشكل صحيح (11 رقم).
- قم بتحويل السعر إلى رقم صحيح بدون رموز أو حروف.
- قم بتقييم مستوى الثقة على مقياس من 0 إلى 100.

قم بالاستخراج الدقيق وتجنب إضافة أي معلومات غير موجودة في الصورة.
`;
};

/**
 * مطالبة استخراج أساسية للوصولات
 */
export const getBasicExtractionPrompt = (): string => {
  return `
استخرج البيانات من صورة وصل الشحن هذا وأعطني فقط النص في شكل JSON:

{
  "code": "رقم الكود",
  "senderName": "اسم المرسل",
  "phoneNumber": "رقم الهاتف",
  "province": "اسم المحافظة",
  "price": "السعر",
  "companyName": "اسم الشركة"
}

أريد فقط البيانات في تنسيق JSON، بدون أي تفسير أو نص إضافي.
`;
};

/**
 * مطالبة استخراج النص فقط
 */
export const getTextOnlyExtractionPrompt = (): string => {
  return `
استخرج جميع النصوص من هذه الصورة. أعطني كل الكلمات التي يمكنك قراءتها من الصورة.

لا تحاول تفسير النص أو تنظيمه، فقط اعطني النص الخام كما هو ظاهر في الصورة.
`;
};
