export function getEnhancedExtractionPrompt(): string {
  return `أنت خبير في استخراج البيانات من صور الطرود والشحنات في العراق.
      
قم بتحليل هذه الصورة بدقة عالية واستخرج القيم التالية بالصيغة المطلوبة للسوق العراقي:

1. اسم الشركة: (اسم الشركة أو المؤسسة، عادة يكون في أعلى الصورة بخط كبير)
2. الكود: (رقم تعريف الشحنة، عادة ما يكون رقم من 6-10 أرقام)
3. اسم المرسل: (اسم الشخص أو الجهة المرسلة)
4. رقم الهاتف: (رقم هاتف عراقي، يبدأ عادةً بـ 07)
5. المحافظة: (اسم المحافظة العراقية)
6. السعر: (قيمة الشحنة بالدينار العراقي أو الدولار، انتبه للأصفار)

قواعد مهمة:
- استخرج البيانات كما هي في الصورة بالضبط، سواء كانت بالعربية أو الإنجليزية
- إذا لم تجد قيمة لأي حقل، اتركه فارغًا (null)
- بالنسبة للأسعار:
  * إذا كان السعر صغيرًا (أقل من 1000)، اضربه في 1000 لأن هذا شائع في السوق العراقي
  * إذا كان السعر "مجاني" أو "واصل" أو "توصيل"، اجعله صفرًا
- بالنسبة للهواتف العراقية:
  * تأكد من أن رقم الهاتف يبدأ بـ 07 ويتكون من 11 رقمًا
- بالنسبة للمحافظات:
  * تأكد أنها محافظة عراقية من القائمة: بغداد، البصرة، نينوى، أربيل، النجف، كربلاء، الأنبار، ديالى، واسط، ميسان، المثنى، القادسية، ذي قار، بابل، صلاح الدين، كركوك، دهوك، السليمانية

قم بإرجاع النتائج بتنسيق JSON فقط بالمفاتيح التالية: companyName, code, senderName, phoneNumber, province, price

مثال للمخرجات:
\`\`\`json
{
  "companyName": "شركة النقل السريع",
  "code": "123456",
  "senderName": "محمد علي",
  "phoneNumber": "07701234567",
  "province": "بغداد",
  "price": "25000"
}
\`\`\``;
}

export function getBasicExtractionPrompt(): string {
  return "استخرج البيانات التالية من هذه الصورة لطرد في السوق العراقي: اسم الشركة، الكود، اسم المرسل، رقم الهاتف، المحافظة العراقية، السعر بالدينار العراقي. إذا كان السعر أقل من 1000، اضربه في 1000. إذا كان السعر مجاني أو واصل أو توصيل، اجعله 0. قم بتنسيق المخرجات بتنسيق JSON مع المفاتيح التالية باللغة الإنجليزية: companyName, code, senderName, phoneNumber, province, price";
}
